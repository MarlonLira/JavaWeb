    private void btCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btCadastrarActionPerformed
       if(validarCampos()){   
       Cliente cliente = new Cliente();
       ClienteData clienteData = new ClienteData();   

       cliente.setNomeCliente(textoNome.getText());
       cliente.setEmailCliente(textoEmail.getText());
       cliente.setEnderecoCliente(textoEndereco.getText());
       cliente.setNumeroCliente(textoNcontato.getText());
       limparCampos();
       btCadastrar.setEnabled(true);
       listar();
          try {
              clienteData.Incluir(cliente);
          } catch (SQLException ex) {
              Logger.getLogger(TelaCadstro.class.getName()).log(Level.SEVERE, null, ex);
          }
        }
    }//GEN-LAST:event_btCadastrarActionPerformed

