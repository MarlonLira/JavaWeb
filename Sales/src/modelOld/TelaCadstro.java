    public boolean validarCampos(){
    if(textoNome.getText().equals("")){
        
        JOptionPane.showMessageDialog(this, "Prenncha campo Nome");
        textoNome.requestFocus();
         return false;
    }
    if(textoEndereco.getText().equals("")){
        
        JOptionPane.showMessageDialog(this, "Prenncha campo Endereço");
        textoNome.requestFocus();
         return false;
    }
    /*if(textoEmail.getText().equals("")){
        
        JOptionPane.showMessageDialog(this, "Prenncha campo E-mail");
        textoNome.requestFocus();
         return false;
    }*/
    
    return true;
    }
    
